package payment;

public class Payment {
		
			String email;
			String packagee;
			String phone;
			
			public String getEmail() {
				return email;
			}


		

			public String getPackagee() {
				return packagee;
			}


	

			public String getPhone() {
				return phone;
			}



			
	
			public Payment(String email, String packagee, String phone) {
				
				this.email = email;
				this.packagee = packagee;
				this.phone = phone;
			}
	
	
	
}

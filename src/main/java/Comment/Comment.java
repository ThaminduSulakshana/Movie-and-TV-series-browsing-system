package Comment;

public class Comment {
	
	

				String comemail ;
				String cheader;
				String comment;
				
				

	public Comment(String comemail, String cheader, String comment) {
	
		this.comemail = comemail;
		this.cheader = cheader;
		this.comment = comment;
	}
	
	
	
	
	
	
	public String getComemail() {
		return comemail;
	}






	
	public String getCheader() {
		return cheader;
	}





	public String getComment() {
		return comment;
	}



	
	

}
